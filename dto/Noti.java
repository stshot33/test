package dto;

public class Noti {
	private String notiType;
	private String notiTitle;
	private int notiTime;
	private String notiURL;
	private int notiCheck;

	public String getNotiType() {
		return notiType;
	}

	public void setNotiType(String notiType) {
		this.notiType = notiType;
	}

	public String getNotiTitle() {
		return notiTitle;
	}

	public void setNotiTitle(String notiTitle) {
		this.notiTitle = notiTitle;
	}

	public int getNotiTime() {
		return notiTime;
	}

	public void setNotiTime(int notiTime) {
		this.notiTime = notiTime;
	}

	public String getNotiURL() {
		return notiURL;
	}

	public void setNotiURL(String notiURL) {
		this.notiURL = notiURL;
	}

	public int getNotiCheck() {
		return notiCheck;
	}

	public void setNotiCheck(int notiCheck) {
		this.notiCheck = notiCheck;
	}

}
